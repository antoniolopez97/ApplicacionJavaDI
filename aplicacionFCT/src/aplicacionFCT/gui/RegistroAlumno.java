    private boolean validarFomulario(){
    String dni=jTextFieldDNIAlumno.getText();
    if(dni.length()<9||dni.length()>9 || dni==null|| "".equals(dni)){
        JOptionPane.showMessageDialog(this, "El dni no puede ser mas grande que 9 y no puede estar vacio","Error",JOptionPane.ERROR_MESSAGE);
        return false;
    }
       return  true;
    }
    
    
    private void jButtonAltaAlumnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAltaAlumnoActionPerformed
      
        String nombre = jTextFieldNombreAlumno.getText();
        String dni = jTextFieldDNIAlumno.getText();
        String telefono = jTextFieldTelefono.getText();
        String email = jTextFieldEmailAlumno.getText();
        String ciclo =(String) jComboBoxCiclo.getSelectedItem();
        String etapa=null;
        if(buttonGroupCiclo.getSelection().toString()=="Junio"){
         etapa="Junio";
        }else if(buttonGroupCiclo.getSelection().toString()=="Septiembre"){
             etapa="Septiembre";
        }
      
         
       
        String fechaInicio = (String) jSpinnerComienzo.getValue();
         String fechaFinal =(String)jSpinnerFin.getValue();
        String empresa = jTextFieldEmpresaAlumno.getText();
        String nonmbreprofesor = jTextFieldNombreProfesor.getText();
        String dniprofesor = jTextFieldDNIProfesor.getText();
        String telefonoprofesor = jTextFieldTelefonoProfesor.getText();
        String emailprofesor=jTextFieldEmailProfesor.getText();
        Alumno alumno = new Alumno(nombre, dni, telefono, email, ciclo, etapa, fechaInicio, fechaFinal, empresa, nonmbreprofesor, dniprofesor, telefonoprofesor, emailprofesor);
        
        LogicaNegocio.aniadir(alumno);
        
        
        
        
        dispose();
    }//GEN-LAST:event_jButtonAltaAlumnoActionPerformed

